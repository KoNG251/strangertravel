<script>
    import moment from 'moment';
    import axios from 'axios';
    import Notiflix from 'notiflix';
    import Swal from 'sweetalert2'

        export default {
            data(){

                return  {
                    user: {
                        email: ''
                    },
                    hotelList: null,
                    page: 1,
                    nextPage: '',
                    prevPage: '',
                    total: 1
                }
            },
            props:{
                hotelRoute: String,
                checkRoute: String,
                csrfToken: String
            },
            methods: {
                getHotel(page){
                    axios.get('/admin/api/get/hotel?page='+page).then(response => {
                        this.hotelList = response.data.message.data.map(hotel => {
                            return {
                                ...hotel,
                                photos: hotel.photos.split(',')
                            };
                        });
                        this.page = response.data.message.current_page
                        this.nextPage = response.data.message.next_page_url
                        this.prevPage = response.data.message.prev_page_url
                        this.total = response.data.message.last_page
                    })
                },
                logout(){
                    Notiflix.Confirm.show(
                        'Warning',
                        'Do you want to logout?',
                        'Okay',
                        'Cancel',
                        () => {
                            axios.post('/admin/api/logout',
                                {
                                    headers:{
                                        'X-CSRF-TOKEN' : this.csrfToken
                                    }
                                }
                            ).then(response => {
                                Notiflix.Report.success(
                                    'SUCCESS',
                                    'Logout successful!',
                                    'Okay',
                                    () => {
                                        window.location.href = response.data.url
                                    }
                                )
                            })
                        }
                    )
                }
            },
            watch: {

            },
            computed: {

            },
            async mounted() {

                await axios.get('/admin/api/get/info').then(response => {
                    this.user = response.data.message
                })

                await this.getHotel(this.page)

            }
        }
    </script>

